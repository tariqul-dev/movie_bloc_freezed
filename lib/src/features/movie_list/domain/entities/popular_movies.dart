class PopularMovies {
  PopularMovies({
    this.page,
    this.results,
    this.totalPages,
    this.totalResults,
  });

  num? page;
  List<Results>? results;
  num? totalPages;
  num? totalResults;

  PopularMovies copyWith({
    num? page,
    List<Results>? results,
    num? totalPages,
    num? totalResults,
  }) =>
      PopularMovies(
        page: page ?? this.page,
        results: results ?? this.results,
        totalPages: totalPages ?? this.totalPages,
        totalResults: totalResults ?? this.totalResults,
      );
}

class Results {
